static int	ft_is_upper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

int	ft_tolower(int c)
{
	if (ft_is_upper(c))
		return (c + ('a' - 'A'));
	return (c);
}
