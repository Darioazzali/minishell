static int	ft_is_lower(int c)
{
	if (c >= 'a' && c <= 'z')
	{
		return (1);
	}
	return (0);
}

int	ft_toupper(int c)
{
	if (ft_is_lower(c))
		return (c - ('a' - 'A'));
	return (c);
}
